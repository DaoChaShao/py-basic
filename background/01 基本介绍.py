# 3.3 文件与命令

# 3.31 Linux 文件
# Linux 系统下的文件名最长可以有 256 个字符
# Linux 系统下的隐藏文件格式为：.文件名
# - 需要用 -a (a -- all)参数才能显示，如：ls -a
# . 代表当前目录 cd . - 显示当前目录
# .. 代表上一级目录 cd .. - 显示上一级目录 相当于返回上级目录

# 3.3.1 查看目录内容
# ls
# ls -a -- 显示所有目录和文件，包括隐藏文件 (a -- all)
# ls -l -- 以列表方式显示文件的详细信息 (l -- list)
# ls -h -- 配合 -l 以人性化的方式显示文件大小 (h -- human)
# -- 单独使用 -h 没有任何效果
# -- 必须配合 -l 共同使用，例：-l -h
# -- 和 -l 最大的不同是：文件大小的显示

# 此外，三种形式可以任意组合，如：ls -lha

# 计算机文件的大小(从小到大，1024)
# 字节 - B(Byte)
# 千 - K(Kibibyte)
# 兆 - M(Mebibyte)
# 千兆 - G(Gigabyte)
# 太 - T(Terabyte)
# 拍 - P(Petabyte)
# 艾 - E(Exabyte)
# 泽 - Z(Zettabyte)
# 尧 - Y(Yottabyte)

# 通配符通配符
# * - 代表任意个数个字符 - 如果*放在结尾，需要加上文件后缀
# ? - 代表任意一个字符，至少 1 个
# [] - 表示可以匹配字符组中的任意一个
# [abc] - 匹配 a、b、c 中的任意一个
# [a-f] - 匹配 a 到 f 范围内的任意一个字符

# 3.3.2 切换目录 (注意空格)
# cd 切换到当前用户的主目录(/home/用户目录)
# cd ~ 切换到当前用户的主目录(/home/用户目录)
# cd . 保持在当前目录不变
# cd .. 切换到上级目录
# cd - 可以在最近两次工作目录之间来回切换

# 3.3.3 创建和删除
# touch
# - 如果文件不存在，可以创建一个空白文件
# - 如果文件已经存在，可以修改文件的末次修改日期

# mkdir
# -p 创建递归目录，如：a/b/c/d
# rm - 删除之后不能恢复
# -f 强制删除，忽略不存在的文件，无需提示
# -r 递归地删除目录下的内容，删除文件夹时必须加此参数

# 3.3.4 拷贝和移动文件
# tree 目录名 - tree -- 以树状图列出文件目录结构
# - -d - 只显示目录

# cp ~/文件路径 目标文件 - copy -- 复制文件或者目录
# - -f -- 已经存在的目标文件直接覆盖，不会提示
# - -i -- 覆盖文件前提示：overwrite ./readme.txt? (y/n [n])
# - -r -- 复制目录
#       -- 若给出的源文件是目录文件，则cp
#       -- 将递归复制该目录下的所有子目录和文件，目标文件必须为一个目录名

# mv 源文件 目标文件 - move -- 移动文件或者目录/文件或者目录重命名

# 3.3.5 查看文件内容
# cat 文件名 - concatenate -- 查看文件内容、创建文件、文件合并、追加文件内容等
# - -b 对非空输出行编号 (可能是：blank)
# - -n 对输出的所有行编号 (可能是：nonblank)
# - Linux 中还有一个 nl 的命令和 cat -b 的效果等价

# more 文件名 - more -- 分屏显示文件内容
# - 和手册的操作方式一样

# grep 搜索文本文件名 - grep -- 搜索文本文件内容
# - -n -- 显示匹配行及行号
# - -v -- 显示不包含匹配文本的所有行(相当于求反)
# - -i -- 忽略大小写

# grep 参数
# grep ^a 或者 grep ke$
# - ^a -- 行首，搜寻以 a 开头的行
# - ke$ -- 行尾，搜寻以 ke 结束的行


# 3.3.6 其他
# echo：在终端中显示参数指定的文字
# - 通常会和 重定向 联合使用
# 重定向 > 和 >>
# - > 表示输出
# -- 如果输出的内容不存在，则会创建一个赋有内容的新文件
# -- 如果输出的内容已存在，则会覆盖文件原有内容或文件
# - >> 表示追加，会将内容追加到已有的文件的末尾

# 管道 |
# linux 中，允许将 一个命令的输出 可以通过 管道 作为 另一个命令的输入
# 常用的命令管道有：
# - grep：ls -lha ~|grep
# - more：ls -lha ~|more
