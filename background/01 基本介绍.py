# linx 终端命令

# 1.常见 7 个操作命令

# 命令 - 对应英文 -- 作用
# ls - list -- 查看当前文件夹下的内容
# pwd - print work directory -- 查看当前所在文件夹
# cd 目录名 - change directory -- 切换文件夹
# touch 文件名 - touch -- 如果文件不存在，新建文件夹
# mkdir 目录名 - make directory -- 创建目录 (目录：文件夹)
# rm 文件名 - remove -- 删除指定文件夹
#   - rm -r 目录名 -- 删除制定目录
# clear - clear -- 清屏

# 2.终端命令格式
# 语法：command [-option] [paramater] - 中间的空格必须有
# - command：命令名，相应功能的英文单词或单词的缩写
# - [-option]：选项，可用来对命令进行控制，也可以省略
# - [paramater]：传给命令的参数，可以是 零个、一个或者多个
# - [] 表示可有可无 (Mac 不需要[])

# 例：rm -r

# 2.1 查询命令帮助信息 - 简要查询
# 语法：command --help

# 2.2 查询命令帮助信息 - 详细查询
# 语法：man command
# - man 是 manual(手册) 的缩写
# -- 操作键 -- 功能
# -- 空格键 -- 显示手册下一屏
# -- enter键 -- 一次滚动手册的下一行
# -- b -- 回滚一屏
# -- f -- 前滚一屏
# -- q -- 退出
# -- /word 搜索 word 字符串

