# 软件安装

# 1.通过 apt 安装/卸载软件
# - apt 是 Advanced Packaging Tool
# - apt 是 Linux 下的一款安装包管理工具
# - apt 可以在终端中方便 安装/卸载/更新软件包

# 1.1 安装软件
# sudo apt install 软件包

# 1.2 卸载软件
# sudo apt remove 软件名

# 1.3 更新已安装的包
# sudo apt upgrade

# 2.软件安装

# 2.1 一个小火车提示
# sudo apt install sl

# 2.2 一个比较漂亮的查看当前进程排名的软件
# sudo apt install htop
