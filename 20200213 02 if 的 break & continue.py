
# break 某些条件成立，退出整个循环

# 吃5个苹果，吃3个吃饱了，第4个和第5个不吃了

i = 1

while i <= 5:
    if i == 4: # 条件：如果吃了3个苹果吃饱了，第4个和第5个苹果就不吃了
        print("吃撑了，不吃了！")
        break
    print(f"我已经吃了：{i}个苹果 ")
    i += 1

print("吃完收工！")




# continue 退出当前循环，进入下一次循环

i = 1

while i <= 5:
    print(f"我吃第{i}个苹果啦！")
    if i == 3:
        print(f"这第{i}个苹果有虫子，不能再吃这个苹果了！")
        i += 1
        continue # 如果使用continue，则在此之前一定要修改计数器，否则进入死循环

    i += 1

